	SUB KPOSY, 10 ;Presiono arriba
	JMP H60
H40:
	CMP AL, 4DH
	JNE H50
	ADD KPOSX, 10	;Presiono derecha
	JMP H60
H50:
	CMP AL,53H		;Salir
	JNE H90
	MOV EXIT, 1
	JMP H90
H60:
	MOV REFRESH,1
H90:
	RET
H10MOVE ENDP

I10TIME PROC NEAR
	MOV AH, 2CH
	INT 21H
	MOV AH, DH
	SUB AH, LASTTIME
	CMP AH, 1
	JB	I90
	MOV LASTTIME, DH
	CALL H10MOVE
	MOV AH,0CH
	INT 21H
I90:
	RET
I10TIME ENDP

